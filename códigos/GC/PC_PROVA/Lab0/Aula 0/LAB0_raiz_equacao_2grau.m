% Calculo das raizes de uma equação do 2° grau.
% Ax² + Bx + c = 0.

% Parametros do sistema:
a = 4;  % Coeficente A da equação.
b = -9;     % Coeficent B da equação.
c = -17.5;   % Coeficiente C da equação.

% Calculo do delta:
delta = sqrt(b^2-4*a*c);    %Calculo do delta.

% Calculo das raizes:
x1 = (-b+delta)/(2*a)   % Calculo da raiz em x1.
x2 = (-b-delta)/(2*a)   % CAlculo da raiz em x2.
